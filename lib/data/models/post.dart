class Post {
  final String id;
  final String titulo;
  final String descricao;
  final String autor;
  final int estrelas;
  final String forumName;

  Post({
    required this.id,
    required this.titulo,
    required this.descricao,
    required this.autor,
    required this.estrelas,
    required this.forumName,
  });
}
